/*
What are APIs - Application Program Interface
- APIs in general is an umbrella term for an interface that code 
uses to interact with some other code/application/hardware

Data Formats
- APIs dont respond with HTML
-- HTMl contains info about page structure. 
-- APIs respond with data not structure
- APIs use formats like XML and JSON
-- These are still text based 

- XML - looks like HTML but is mostly just key-value information

- JSON - JavaScript Object Notation
-- All keys must be double quoted 

Curl Requests 
- Not done through JS 
- Done through terminal 

API Documentation
- Query strings need to be separated by &
- Value you are searching for denoted with = 
- Query strings need to match the type of data that the API has
-- Can't blindly search for a key-value pair. 


*/